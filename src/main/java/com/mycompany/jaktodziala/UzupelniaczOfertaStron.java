    private void StworzActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StworzActionPerformed
     
         String nazwa = NazwaText.getText();
        String cenaStrona = CenaStronaText.getText();
        String cenaSklep = CenaSklepText.getText();
        String autor = AutorText.getText();
        String data = DataText.getText();
        
              if(NazwaText.getText().equalsIgnoreCase("")||CenaStronaText.getText().equalsIgnoreCase("")||
                CenaSklepText.getText().equalsIgnoreCase("")
                ||AutorText.getText().equalsIgnoreCase("")
                ||DataText.getText().equalsIgnoreCase("")
                
                )
        {
               JOptionPane.showMessageDialog(null, "Uzupe≈Çnij wszystkie pola zanim stworzysz Oferte"); 
        }
              else
              {
        Document pdfDocument = new Document("ofertaSklep.pdf");
        MetodyWczytujaceOfertyStrony.NazwaFirmy(pdfDocument, nazwa);
        MetodyWczytujaceOfertyStrony.CenaStrony(pdfDocument, cenaStrona);
        MetodyWczytujaceOfertyStrony.CenaSklepu(pdfDocument, cenaSklep);
        MetodyWczytujaceOfertyStrony.DataOferty(pdfDocument, data);
        MetodyWczytujaceOfertyStrony.AutorOferty(pdfDocument, autor);
        
        pdfDocument.save("./OfertyStronInternetowych/"+MetodyWczytujaceOfertyStrony.nazwaFirmy+"Oferta.pdf");
        JOptionPane.showMessageDialog(null, "Oferta zosta≈Ça Stworzona");
              }
    }//GEN-LAST:event_StworzActionPerformed

